window.KOMPUTERRA_normalizeAssetPath = function(path) {
  if (!path) return '';
  if (path === '#') return '#';
  return path.replace(/^\.\//, '').replace(/^\.\.\//, '');
};

window.KOMPUTERRA_placeholderForCategory = function(category) {
  if ((category || '').includes('акумулятори')) return 'assets/placeholder-battery.svg';
  if ((category || '').includes('панелі')) return 'assets/placeholder-panel.svg';
  return 'assets/placeholder-inverter.svg';
};

window.KOMPUTERRA_reconcileProducts = function(products) {
  const defaults = window.KOMPUTERRA_DEFAULT_PRODUCTS || [];
  const byId = new Map(defaults.map(item => [item.id, item]));
  const byModel = new Map(defaults.map(item => [item.model, item]));
  const byName = new Map(defaults.map(item => [item.name, item]));
  return (products || []).map((item) => {
    const fallback = byId.get(item.id) || byModel.get(item.model) || byName.get(item.name) || {};
    const merged = Object.assign({}, fallback, item);
    merged.image = window.KOMPUTERRA_normalizeAssetPath(merged.image || fallback.image || window.KOMPUTERRA_placeholderForCategory(merged.category));
    merged.pdf = window.KOMPUTERRA_normalizeAssetPath(merged.pdf || fallback.pdf || '#') || '#';
    if (!merged.image || merged.image === '#') {
      merged.image = window.KOMPUTERRA_placeholderForCategory(merged.category);
    }
    if (!merged.pdf) merged.pdf = '#';
    return merged;
  });
};
